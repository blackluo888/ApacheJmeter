package org.apache.jmeter.util.entity;

public class JSONVarEntity {

	private Object varName; 
	private Object oldProperty;
	
	
	
	
	public Object getVarName() {
		return varName;
	}
	public void setVarName(Object varName) {
		this.varName = varName;
	}
	public Object getOldProperty() {
		return oldProperty;
	}
	public void setOldProperty(Object oldProperty) {
		this.oldProperty = oldProperty;
	}
	
	
	
}
